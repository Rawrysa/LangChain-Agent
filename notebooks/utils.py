def add(a, b):
    """
    Add two numbers together.
    """
    return a + b

def subtract(a, b):
    """
    Subtract two numbers.
    """
    return a - b

def multiply(a, b):
    """
    Multiply two numbers.
    """
    return a * b

def divide(a, b):
    """
    Divide two numbers.
    """
    return a / b if b != 0 else "Cannot divide by zero"